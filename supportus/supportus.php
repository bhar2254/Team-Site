		  <h2>Our Sponsors are Invaluable...</h2>
			<p>Sponsors are the divine force that keeps our robotics program afloat. We are very appreciative of the generous businesses and friends which have found FIRST<sup>&reg;</sup> Team 1764 to be a worthwhile endeavor in which to invest their time and assets. Each logo at the side of this page represents individuals and companies who have made substantial contributions to our program.</p>
			<br/>
			<h2>Sponsorship Levels</h2>
			<p>We recognize several levels of sponsorship which is determined by the contribution any given company has provided. The levels from highest to lowest include: Founding Sponsors, Diamond Sponsors, Gold Sponsors, Silver Sponsors, and Friends of FIRST<sup>&reg;</sup> Team 1764. Please visit the <a href="info">About Sponsorship Levels</a> page to view our table of sponsorship levels. Many of the items donated are tax-deductible, either as a donation to the Liberty Public School System, or through the 501c3 <a target="_blank" href="http://www.libertyroboticsfoundation.org/">Liberty Robotics Foundation</a>.
			</p>
			<br/>
			<h2>What Denotes a Sponsor</h2>
			<p>Sponsorships can be established by the donation of virtually anything; labor, materials, supplies and monetary means are all viable forms of sponsorship. Whether your donation is large or small; has a large or small impact, it is very much needed and appreciated by the team! From the donation of donuts for breakfast to the funds that enable us to travel to competitions, your efforts are valued and will be reflected in our commitment to promoting your business.</p>
			<br/>
			<h2>The Circle of Friends</h2>
			<p>Please be watching for the newest edition of our sponsorship family "The Circle of Friends". We are excited to announce the initiation of this Liberty Robotics Foundation project. As developed, the Liberty Robotics Foundation in conjunction with FIRST<sup>&reg;</sup> Team 1764, will challenge 100 business owners and robotics friends to contribute $100 each. Almost anyone can afford to be a sponsor in this program!</p>
			<br/>
			<h2>How to Become a Sponsor</h2>
			<p>Becoming a sponsor is as easy as expressing interest in our team and activities. If you want more information about FIRST<sup>&reg;</sup> Team 1764 all you have to do is ask. We have a panel of students who are ready and available to present information at your business or function. We are very proud of our accomplishments and we think you will be too.</p>
			<p>For anyone who is interested in becoming a sponsor, or if you just want to be educated about FIRST<sup>&reg;</sup> please contact Head Coach, <a href="mailto:gpierson@liberty.k12.mo.us">Gary Pierson</a> or Liberty Robotics Foundation President, Terry Hayter.</p>
