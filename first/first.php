				<h2 class="featurette-heading">Gracious Professionalism</h2>
				<p class="lead">Gracious Professionalism is a concept that has been much embraced by the FIRST<sup>&reg;</sup> community. It's purpose is to encourage generosity while embodying the acts of professionalism. This semi-ironic term was coined by Dr. Woodie Flowers, who says it best, <em>"Gracious professionals learn and compete like crazy, but treat one another with respect and kindness in the process. They avoid treating anyone like losers. No chest thumping tough talk, but no sticky-sweet platitudes either. Knowledge, competition, and empathy are comfortably blended."</em></p>
				<p class="paragraph">The meaning speaks volumes for the priorities of the FIRST<sup>&reg;</sup> Community. In this society students are taught that winning is not everything, and the most important victory is what you learn in the process.</p>
